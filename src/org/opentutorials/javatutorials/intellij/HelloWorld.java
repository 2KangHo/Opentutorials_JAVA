package org.opentutorials.javatutorials.intellij;

public class HelloWorld {
    /*
    * static
    *  같은 클래스로 생성된 객체들이 같은 값을 공유하게 하는 키워드
    *
    *  이 키워드가 붙은 것은 프로그램 시작 시 제일 먼저 메모리에 올라감
    *  (메모리에 제일 먼저 올려 프로그램의 시작점인 main 함수를 쉽게 찾을 수 있도록 하기 위해 static 키워드가 main 함수에 붙음)
    */
    public static void main(String[] args) {
        System.out.println("Hello World!");
    }
}